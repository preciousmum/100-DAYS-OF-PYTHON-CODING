# This conditional statement allows the computer to take certain conditions and perform a action.
# if condition:
#    do this
# else:
#   do this
# Don't forget your colon ":" at the end of your condition as it helps with the indentation

print("Welcome to rollercoaster!")
height = int(input("What is your height in cm? "))

# Conditional Statement
if height > 120:
    print("You can ride the rollercoaster!")
else:
    print("Sorry, come back when you grow taller")

